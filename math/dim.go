package math

func MaxInt64(x, y int64) int64 {
	if x > y {
		return x
	} else {
		return y
	}
}

func MinInt64(x, y int64) int64 {
	if x < y {
		return x
	} else {
		return y
	}
}

func MaxInt(x, y int) int {
	if x > y {
		return x
	} else {
		return y
	}
}

func MinInt(x, y int) int {
	if x < y {
		return x
	} else {
		return y
	}
}